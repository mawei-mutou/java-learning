package yzbtest.date20200101;

public class TestSwitch {
    public static void main(String[] args) {
        String inputStr = "aaa";
        switch (inputStr){
            case "aaa":
                System.out.println("it's aaa!");
                break;
            case "bbb":
                System.out.println("it's bbb!");
                break;
            default:
                System.out.println("it's default!");
        }
    }
}
